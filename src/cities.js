/**
 * Look up a city in the table. Normalizes the input (lowercase, underscore).
 * Returns { lat, lng } or null if not found.
 */
export function cityCoords(cityRaw) {
  if (!cityRaw) return null;
  const key = cityRaw.toLowerCase().replace(/\s+/g, '_');
  const v = CITIES[key];
  return v ? { lat: v[0], lng: v[1] } : null;
}
